package ch.so.agi.cadastralinfo.models.grundbuch;

public class HaengigesGeschaeft {
    private String egbtbid;
    
    private String tagebuchNummer;
    
    private String tagebuchDatumZeit;
    
    private String geschaeftsfallbeschreibungStichwort;
    
    private String geschaeftsfallbeschreibungZusatz;

    public String getEgbtbid() {
        return egbtbid;
    }

    public void setEgbtbid(String egbtbid) {
        this.egbtbid = egbtbid;
    }

    public String getTagebuchNummer() {
        return tagebuchNummer;
    }

    public void setTagebuchNummer(String tagebuchNummer) {
        this.tagebuchNummer = tagebuchNummer;
    }

    public String getTagebuchDatumZeit() {
        return tagebuchDatumZeit;
    }

    public void setTagebuchDatumZeit(String tagebuchDatumZeit) {
        this.tagebuchDatumZeit = tagebuchDatumZeit;
    }

    public String getGeschaeftsfallbeschreibungStichwort() {
        return geschaeftsfallbeschreibungStichwort;
    }

    public void setGeschaeftsfallbeschreibungStichwort(String geschaeftsfallbeschreibungStichwort) {
        this.geschaeftsfallbeschreibungStichwort = geschaeftsfallbeschreibungStichwort;
    }

    public String getGeschaeftsfallbeschreibungZusatz() {
        return geschaeftsfallbeschreibungZusatz;
    }

    public void setGeschaeftsfallbeschreibungZusatz(String geschaeftsfallbeschreibungZusatz) {
        this.geschaeftsfallbeschreibungZusatz = geschaeftsfallbeschreibungZusatz;
    }

}
